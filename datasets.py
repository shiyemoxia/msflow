MVTEC_CLASS_NAMES = ['bottle', 'cable', 'capsule', 'carpet', 'grid',
               'hazelnut', 'leather', 'metal_nut', 'pill', 'screw',
               'tile', 'toothbrush', 'transistor', 'wood', 'zipper']

class MVTecDataset(Dataset):
    def __init__(self, c, is_train=True):
        assert c.class_name in MVTEC_CLASS_NAMES, 'class_name: {}, should be in {}'.format(c.class_name, MVTEC_CLASS_NAMES)
        self.dataset_path = c.data_path
        self.class_name = c.class_name
        self.is_train = is_train
        self.input_size = c.input_size
        # load dataset
        self.x, self.y, self.mask = self.load_dataset_folder()
        # set transforms
        if is_train:
            self.transform_x = T.Compose([
                T.Resize(c.input_size, InterpolationMode.LANCZOS),
                T.ToTensor()])
        # test:
        else:
            self.transform_x = T.Compose([
                T.Resize(c.input_size, InterpolationMode.LANCZOS),
                T.ToTensor()])
        # mask
        self.transform_mask = T.Compose([
            T.Resize(c.input_size, InterpolationMode.NEAREST),
            T.ToTensor()])

        self.normalize = T.Compose([T.Normalize(c.img_mean, c.img_std)])

    def __getitem__(self, idx):
        x, y, mask = self.x[idx], self.y[idx], self.mask[idx]
        #x = Image.open(x).convert('RGB')
        x = Image.open(x)
        if self.class_name in ['zipper', 'screw', 'grid']:  # handle greyscale classes
            x = np.expand_dims(np.array(x), axis=2)
            x = np.concatenate([x, x, x], axis=2)
            
            x = Image.fromarray(x.astype('uint8')).convert('RGB')
        #
        x = self.normalize(self.transform_x(x))
        #
        if y == 0:
            mask = torch.zeros([1, *self.input_size])
        else:
            mask = Image.open(mask)
            mask = self.transform_mask(mask)

        return x, y, mask

    def __len__(self):
        return len(self.x)

    def load_dataset_folder(self):
        phase = 'train' if self.is_train else 'test'
        x, y, mask = [], [], []

        img_dir = os.path.join(self.dataset_path, self.class_name, phase)
        gt_dir = os.path.join(self.dataset_path, self.class_name, 'ground_truth')

        img_types = sorted(os.listdir(img_dir))
        for img_type in img_types:

            # load images
            img_type_dir = os.path.join(img_dir, img_type)
            if not os.path.isdir(img_type_dir):
                continue
            img_fpath_list = sorted([os.path.join(img_type_dir, f)
                                     for f in os.listdir(img_type_dir)])
            x.extend(img_fpath_list)

            # load gt labels
            if img_type == 'good':
                y.extend([0] * len(img_fpath_list))
                mask.extend([None] * len(img_fpath_list))
            else:
                y.extend([1] * len(img_fpath_list))
                gt_type_dir = os.path.join(gt_dir, img_type)
                img_fname_list = [os.path.splitext(os.path.basename(f))[0] for f in img_fpath_list]
                gt_fpath_list = [os.path.join(gt_type_dir, img_fname + '_mask.png')
                                 for img_fname in img_fname_list]
                mask.extend(gt_fpath_list)

        assert len(x) == len(y), 'number of x and y should be same'

        return list(x), list(y), list(mask)


class SMAPDataset(Dataset):
    def __init__(self, c, is_train=True):
        self.dataset_path = c.data_path
        self.is_train = is_train
        self.input_size = c.input_size

        labels_path = os.path.join(os.path.dirname(os.path.dirname(self.dataset_path)), 'labeled_anomalies.csv')
        df = pd.read_csv(labels_path)
        df = df[df['spacecraft'] == 'SMAP']
        self.smap_ids = sorted(df['chan_id'].unique().tolist())

        self.label_map = {}
        for _, row in df.iterrows():
            chan_id = row['chan_id']
            seq = str(row['anomaly_sequences'])
            has_anomaly = '[]' not in seq and seq.strip() != '[]'
            self.label_map[chan_id] = 1 if has_anomaly else 0

        split = 'train' if self.is_train else 'test'
        split_dir = os.path.join(self.dataset_path, split)

        self.x = []
        self.y = []
        self.mask = []

        for chan_id in self.smap_ids:
            fname = chan_id + '.npy'
            fpath = os.path.join(split_dir, fname)
            if not os.path.exists(fpath):
                continue
            self.x.append(fpath)
            if self.is_train:
                self.y.append(0)
            else:
                self.y.append(self.label_map.get(chan_id, 0))
            self.mask.append(None)

        self.normalize = T.Compose([T.Normalize(c.img_mean, c.img_std)])
        if hasattr(c, 'smap_used_dims'):
            self.used_dims = c.smap_used_dims
        else:
            self.used_dims = [0, 1, 2]

    def __getitem__(self, idx):
        x_path = self.x[idx]
        y = self.y[idx]
        arr = np.load(x_path)
        if arr.ndim == 1:
            arr = arr[:, None]
        T_len, D = arr.shape
        dims = [d for d in self.used_dims if d < D]
        arr = arr[:, dims]
        arr = arr.T
        x = torch.from_numpy(arr).float()
        x = x.unsqueeze(0).unsqueeze(2)
        x = F.interpolate(x, size=self.input_size, mode='bilinear', align_corners=False)
        x = x.squeeze(0)
        x = self.normalize(x)
        mask = torch.zeros([1, *self.input_size])
        return x, y, mask

    def __len__(self):
        return len(self.x)


